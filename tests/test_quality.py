class TestSectionParser:

    def test_parse_well_structured(self, parser):
        sections = parser.parse(WELL_STRUCTURED_RCT)
        summary = parser.get_section_summary(sections)

        assert summary['has_abstract'], "Should detect abstract"
        assert 'methods' in sections, "Should detect methods"
        assert 'results' in sections, "Should detect results"
        assert 'discussion' in sections, "Should detect discussion"

    def test_imrad_complete(self, parser):
        sections = parser.parse(WELL_STRUCTURED_RCT)
        summary = parser.get_section_summary(sections)
        # May not detect 'introduction' if heading says 'Introduction'
        # but should detect most IMRaD sections
        detected = set(sections.keys()) - {'full_text'}
        assert len(detected) >= 4, f"Should find at least 4 sections, found: {detected}"

    def test_methods_subsections(self, parser):
        sections = parser.parse(WELL_STRUCTURED_RCT)
        if 'methods' in sections:
            methods = sections['methods']
            sub_names = [s.name for s in methods.subsections]
            # Should find at least some of: participants, randomization, blinding, etc.
            assert len(sub_names) >= 2, f"Should find methods subsections, found: {sub_names}"

    def test_empty_text(self, parser):
        sections = parser.parse(EMPTY_TEXT)
        assert 'full_text' in sections
        assert len(sections) >= 1

    def test_short_text(self, parser):
        sections = parser.parse(SHORT_TEXT)
        assert 'full_text' in sections

    def test_poorly_structured(self, parser):
        sections = parser.parse(POORLY_REPORTED_RCT)
        # Should still return full_text even without clear headings
        assert 'full_text' in sections

    def test_section_aliases(self):
        """Test that section alias mapping works."""
        secs = get_expected_sections('Methods - Blinding')
        assert 'methods' in secs

        secs = get_expected_sections('Results - Harms')
        assert 'results' in secs

        secs = get_expected_sections('Nonexistent Section')
        assert 'full_text' in secs


