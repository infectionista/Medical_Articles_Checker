class TestEnhancedCheckerQuality:

    def test_well_structured_high_compliance(self, checker):
        """A well-structured RCT should score >80% compliance."""
        result = checker.check(WELL_STRUCTURED_RCT, 'CONSORT')

        assert result.weighted_score > 70, (
            f"Well-structured RCT should score >70%, got {result.weighted_score}%"
        )
        assert result.simple_score > 70, (
            f"Simple score should be >70%, got {result.simple_score}%"
        )

    def test_well_structured_ground_truth(self, checker):
        """Check that specific items are detected as present."""
        result = checker.check(WELL_STRUCTURED_RCT, 'CONSORT')

        detected_present = set()
        for item in result.items:
            if item.verdict in ('present', 'partial'):
                detected_present.add(item.item_id)

        # At least 80% of expected-present items should be found
        expected = WELL_STRUCTURED_EXPECTED_PRESENT
        true_positives = detected_present & expected
        recall = len(true_positives) / len(expected)

        assert recall >= 0.80, (
            f"Recall too low: {recall:.0%}. "
            f"Missed: {expected - detected_present}"
        )

    def test_poorly_reported_low_compliance(self, checker):
        """A poorly reported study should score <50% compliance."""
        result = checker.check(POORLY_REPORTED_RCT, 'CONSORT')

        assert result.weighted_score < 50, (
            f"Poorly reported RCT should score <50%, got {result.weighted_score}%"
        )

    def test_poorly_reported_detects_absence(self, checker):
        """Items known to be absent should be detected as absent."""
        result = checker.check(POORLY_REPORTED_RCT, 'CONSORT')

        detected_absent = set()
        for item in result.items:
            if item.verdict in ('absent', 'explicitly_absent'):
                detected_absent.add(item.item_id)

        expected_absent = POORLY_REPORTED_EXPECTED_ABSENT
        true_negatives = detected_absent & expected_absent
        specificity = len(true_negatives) / len(expected_absent)

        assert specificity >= 0.70, (
            f"Specificity too low: {specificity:.0%}. "
            f"False positives on: {expected_absent - detected_absent}"
        )

    def test_empty_text_no_crash(self, checker):
        """Empty text should not crash."""
        result = checker.check(EMPTY_TEXT, 'CONSORT')
        assert result.weighted_score == 0
        assert result.total_items > 0

    def test_short_text_low_score(self, checker):
        """Very short text should score near zero."""
        result = checker.check(SHORT_TEXT, 'CONSORT')
        assert result.weighted_score < 20

    def test_section_awareness(self, checker):
        """Keywords found in the right section should score higher."""
        result = checker.check(WELL_STRUCTURED_RCT, 'CONSORT')

        section_matched_items = [i for i in result.items if i.section_match]
        fallback_items = [i for i in result.items if not i.section_match and i.matched_keywords]

        # Items matched in correct sections should have higher average confidence
        if section_matched_items and fallback_items:
            avg_section = sum(i.confidence for i in section_matched_items) / len(section_matched_items)
            avg_fallback = sum(i.confidence for i in fallback_items) / len(fallback_items)
            # We just check that section-matched are not worse than fallback
            assert avg_section >= avg_fallback * 0.8, (
                f"Section-matched items ({avg_section:.2f}) should not score "
                f"much lower than fallback ({avg_fallback:.2f})"
            )


