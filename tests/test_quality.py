class TestEnhancedVsOriginal:
    """
    These tests verify the enhanced checker is at least as good as the
    original, or better. They are designed to highlight specific
    improvements.
    """

    def test_no_generic_keyword_false_positives(self, checker):
        """
        The original checker marked item 1b as 'found' because 'background',
        'methods', 'results' appeared *somewhere* in the text. The enhanced
        checker should only match when these appear in the abstract context.
        """
        # Article with NO abstract but contains 'methods', 'results' elsewhere
        no_abstract_text = """
        A study about something.

        Methods
        We did some experiments. The methods were standard.

        Results
        We found some results. The background of the patients was varied.
        """
        result = checker.check(no_abstract_text, 'CONSORT')

        item_1b = next(i for i in result.items if i.item_id == '1b')
        # With enhanced keywords ('structured abstract', 'trial design', etc.),
        # this should NOT match strongly
        assert item_1b.confidence < 0.6, (
            f"Item 1b (structured abstract) should not be confident without "
            f"actual abstract structure. Got confidence: {item_1b.confidence}"
        )

    def test_distinguishes_well_vs_poorly_reported(self, checker):
        """
        The enhanced checker should produce a significantly higher score
        for the well-structured RCT than for the poorly reported one.
        """
        good_result = checker.check(WELL_STRUCTURED_RCT, 'CONSORT')
        poor_result = checker.check(POORLY_REPORTED_RCT, 'CONSORT')

        gap = good_result.weighted_score - poor_result.weighted_score
        assert gap >= 25, (
            f"Score gap between good and poor articles should be >=25 points. "
            f"Good: {good_result.weighted_score}, Poor: {poor_result.weighted_score}, "
            f"Gap: {gap}"
        )


